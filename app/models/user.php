	public function recoverPassword($email, $user)
  	{
  	$fullName = $user['User']['name'].' '.$user['User']['lastname'];

  	$password = trim((string)User::randomPassword());

   	$Email = new CakeEmail();
    $Email->config('smtp');
    $Email->from(array('artesucvbvmjm@gmail.com' => 'bvmjm'))
    ->template('forgot', 'default')
    ->viewVars(array('name' => $fullName, 'password' => $password))
    ->emailFormat('html')
    ->to($email)
    ->subject('bvmjm, Recuperación de Contraseña de Administrador')
    ->send();

    $this->data['User']['password'] = $password;
  	$this->save();
  }
